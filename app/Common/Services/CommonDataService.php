	/*------------------------------------------------------------------------------
  | Decodes bcrypt value
  --------------------------------------------------------------------------------*/

  public function decrypt_value1($value)
  {
    $decrypted = decrypt($value);
    return $decrypted;
  }

  /*------------------------------------------------------------------------------
  | Ends
  --------------------------------------------------------------------------------*/

  /*------------------------------------------------------------------------------
  | Encodes bcrypt value
  --------------------------------------------------------------------------------*/

  public function encrypt_value1($value)
  {
    $encrypted = encrypt($value);
    return $encrypted;
  }

  /*------------------------------------------------------------------------------
  | Ends
  --------------------------------------------------------------------------------*/
}

